#include "minishell.h"

t_token	*next_sep(t_token *token, int skip)
{
	if (token && skip)
		token = token->next;
	while (token && token->type < TOKEN_TRUNC)
		token = token->next;
	return (token);
}

t_token	*prev_sep(t_token *token, int skip)
{
	if (token && skip)
		token = token->prev;
	while (token && token->type < TOKEN_TRUNC)
		token = token->prev;
	return (token);
}

t_token	*next_run(t_token *token, int skip)
{
	if (token && skip)
		token = token->next;
	while (token && token->type != TOKEN_CMD)
	{
		token = token->next;
		if (token && token->type == TOKEN_CMD && token->prev == NULL)
			;
		else if (token && token->type == TOKEN_CMD && token->prev->type < TOKEN_CMD)
			token = token->next;
	}
	return (token);
}
